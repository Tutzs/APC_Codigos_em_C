#include <stdio.h>

int main()
{
    int c, a, n;

    scanf("%d %d", &c, &a);
    c = c - 1;
    if (a % c > 0)
    {
        n = (a / c) + 1;
        printf("%d\n", n);
    }
    else
    {
        if (n = a / c)
        {
            printf("%d\n", n);
        }
    }
    return 0;
}